# InsureChat v3.0 — MCP Server Package
# This package implements the Model Context Protocol (MCP) server that exposes
# all InsureChat tools (input validation, embedding, retrieval, LLM call,
# output validation, ingestion, config, and logging) as MCP-callable tools.
# An MCP client (e.g. Claude Desktop, VS Code Copilot) can discover and invoke
# these tools via the FastMCP framework.
